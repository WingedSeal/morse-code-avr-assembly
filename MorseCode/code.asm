	; [3:8] = 0 for . , 1 for - lead by 0 padding
	.db 0b010_000_01, 0b100_0_1000 ; A .- , B -...
	.db 0b100_0_1010, 0b011_00_100 ; C -.-. , D -..
	.db 0b001_0000_0, 0b100_0_0010 ; E . , F ..-.
	.db 0b011_00_110, 0b100_0_0000 ; G --. , H ....
	.db 0b010_000_00, 0b100_0_0111 ; I .. , J .---
	.db 0b011_00_101, 0b100_0_0100 ; K -.- , L .-..
	.db 0b010_000_11, 0b010_000_10 ; M -- , N -.
	.db 0b011_00_111, 0b100_0_0110 ; O --- , P .--.
	.db 0b100_0_1101, 0b011_00_010 ; Q --.- , R .-.
	.db 0b011_00_000, 0b001_0000_1 ; S ... , T -
	.db 0b011_00_001, 0b100_0_0001 ; U ..- , V ...-
	.db 0b011_00_011, 0b100_0_1001 ; W .-- , X -..-
	.db 0b100_0_1011, 0b100_0_1100 ; Y -.-- , Z --..
	.db 0b101__01111, 0b101__00111 ; 1 .---- , 2 ..---
	.db 0b101__00011, 0b101__00001 ; 3 ...-- , 4 ....-
	.db 0b101__00000, 0b101__10000 ; 5 ..... , 6 -....
	.db 0b101__11000, 0b101__11100 ; 7 --... , 8 ---..
	.db 0b101__11110, 0b101__11111 ; 9 ----. , 0 -----
